        <h2>Алгебраическая нотация</h2>
        <p>Изобретена арабским шахматистом и шахматным композитором Филиппом Стаммой в 18 веке н.э. и усовершенствована немецким шахматистом М. Гришелем (1784). Используется в большинстве стран мира.</p>
        <p>Поля шахматной доски по горизонтали обозначаются буквами латинского алфавита (a, b, c, d, e, f, g, h), а по вертикали арабскими цифрами от 1-го до 8-ми. Каждое поле шахматной доски имеет свой адрес – пересечение вертикали и горизонтали (например: а1, d6, f3, g7 и т.д.).</p>
        <p>Фигуры обозначаются буквами или изображения фигур:</p>
        <img class="img-fluid float-start" style="margin-right: 20px;" src="img/notation.jpg" alt="NOTATION" width="300" height="300" />
        <p>Король – Кр или <span class="f2">K</span>(в английской нотации К от слова King);</p>
        <p>Ферзь – Ф или <span class="f2">I</span> (Q - Queen);</p>
        <p>Ладья – Л или <span class="f2">G</span> (R – Rook);</p>
        <p>Слон – С или <span class="f2">E</span>(B – Bishop);</p>
        <p>Конь – К или <span class="f2">C</span>(N – kNight);</p>
        <p>Пешка при записи ходов не обозначается.</p>

        <p><span class="f2">abcdefghijklmnopqrstuvxyz ABCDEFGHIJKLMNOPQRSTUVXYZ</span></p>

        <p>Запись ходов в партии бывает в <b><em>полной нотации</em></b> и в <b><em>сокращенной</em></b>.</p>
        <p>В <em><b>полной нотации</b></em> отмечается перемещение фигуры с покинутого поля на поле в результате хода. Например: Кg1-f3, (пешка) e2-e4, Фd1-a4. Если фигура (пешка) бьет фигуру (пешку) противника, то отображается в полной записи это при помощи знака «х» или «:» , т.е. например, Сf1хa6 или Лb2:h2.</p>
        <p>В <em><b>сокращенной нотации</b></em> отображается только название фигуры и то поле, куда она переместилась. Например: Фa6, Лd4, Кf8, bxc3. Если происходит взятие на проходе, то обозначается поле, на которое встала пешка после взятия и к записи дописывается «е.р.». Например, схd3 e.р.</p>
        <p>При записи хода указывают его порядковый номер. Очередность ходов считают от хода белых. Например начало партии может быть записано так: 1. е4 е5 2. Кf3 Кc6 3.Сс4 Сс5 и т.д.</p>
        <p>Если две одинаковые фигуры могут пойти на одно поле, то указывают дополнительно с какой вертикали или горизонтали пошла фигура. Например, Ксе4, Лсс5, Л8а3 и т.д.</p>
        <p>Когда пешка дошла до последней горизонтали и превращается в другую фигуру, указывается обозначение фигуры, в которую она превратилась. Например: а8Ф, cxb1Л, f8К и т.д.</p>
        <p>Рокировка обозначается так:</p>
        <p>Короткая рокировка – 0-0;</p>
        <p>Длинная рокировка – 0-0-0;</p>
        <p>В случае шаха пишут знак «+», например: Ла4+ или Ф:f6+ и т.д.</p>
        <p>Обозначение Мата «#» или «Х» в конце записи хода, например, Фс4# или Cа3Х</p>
        <p>Двойной шах обозначается знаком «++».</p>
        <p>Результат партии записывается так:</p>
        <p>1:0 - белые выиграли;</p>
        <p>0:1 - черные выиграли;</p>
        <p>1/2 : 1/2 -ничья.</p>
