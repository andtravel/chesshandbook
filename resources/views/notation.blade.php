        <a name="02"><h2 class="text-center">Цифровая нотация</h2></a>
        <p>Применяется в международных соревнованиях по переписке (принята как обязательная нотация). Введена по предложению И. Савенкова в качестве кода для передачи ходов по телеграфу. Впервые опробована в матче Красноярск – Петербург (1886-1887 гг.)</p>
        <p>В основе цифровой нотации лежит код, обозначающий каждое поле шахматной доски двузначной цифрой:</p>
        <img class="img-fluid mx-auto d-block" style="margin-right: 20px;" src=" img/numbers-notation.jpg" alt="NUMBER NOTATION" width="300" height="300" />
        <p>Записывается ход фигуры четырехзначным числом: с какого поля взята фигура на поле, которое переместилась; рокировка обозначается координатами перемещения короля, к примеру, короткая рокировка белого короля обозначается 5171, длинная 5131.</p>
        <p> При достижении пешкой последней горизонтали добавляется код фигуры, в которую она превратилась: 1 – ферзь, 2 – ладья, 3 – слон, 4 – конь.</p>
        <p>Пример: алгебраическая нотация <b>1.е4 е5 2.Кf3 Кc6 3.Сс4 Сс5 4. b4 С:b4</b></p>
        <p>цифровая <b>1. 5254 5755 2. 7163 2836 3. 6134 6835 4. 2224 3524</b></p>
        <a name="03"><h2 class="text-center">Код Удемана</h2></a>
        <p>Принят по предложению американского шахматиста Л. Удемана (1882). Применяется для передачи ходов в соревнованиях по телеграфу и по радио.</p>
        <p>Поля шахматной доски обозначаются сочетанием согласной и гласной букв латинского алфавита:</p>
        <img class="img-fluid mx-auto d-block" src="img/kod-udeman.jpg" alt="KOD UDEMAN" width="300" height="300" /></a>
        <p>В радио- или телеграмме указываются поля передвижения фигуры (пешки); при рокировке – ход короля.</p>
        <p>Вначале своего сообщения каждая сторона в соответствии с общепринятыми нормами в заочных соревнованиях повторяет полученный код партнера, а затем передает свой.</p>
        <p>Пример: алгебраическая нотация <b>1.е4 е5 2.Кf3 Кc6 3.Сс4 Сс5 4. b4 С:b4</b></p>
        <p>КодУдемана: <b>gego seso, seso kahi napi, napi hado tapo, tapo ceco poco</b></p>
