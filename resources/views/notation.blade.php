        <a name="01"><h2 class="text-center">Описательная нотация</h2></a>
        <p>Данного вида нотация принята в испано- и англоязычных странах.</p>
        <p>Вертикали обозначены начальными буквами фигур (на английском языке), находящихся на них в начальной позиции:</p>
        <ul>
            <li>Король – <b>К</b> – (King)</li>
            <li>Ферзь – <b>Q</b>– (Queen)</li>
            <li>Ладья – <b>R</b> – (Rook)</li>
            <li>Слон – <b>B</b> – (Bishop)</li>
            <li>Конь – <b>Kt</b> – (Knight)</li>
            <li>Пешка – <b>P</b> – (Pown)</li>
        </ul>
        <p>Горизонтали обозначены арабскими цифрами от 1 до 8.</p>
        <p>В отличие от алгебраической нотации, где отсчет идет от белых и номер горизонтали неизменный, в описательной нотации каждая сторона отсчет ведет от себя.</p>
        <p> Например, ход Kd4 в алгебраической нотации может быть сделан как белыми так и черными, но в описательной ход белых Kt– Q4 , а ход черных описывается как Kt – Q5.</p>
        <p> При описании ходов, сделанных на флангах указывается королевский (К) и ферзевый (Q) фланги. Взятие обозначается знаком «х», шах – ch, мат – mate.</p>
        <p>Пример: начало партии в алгебраической нотации <b>1.е4 е5 2.К</b><b>f</b><b>3 К</b><b>c</b><b>6 3.Сс4 Сс5 4. </b><b>b</b><b>4 С:</b><b>b</b><b>4</b> в описательной запишется так: <b>1. </b><b>P</b><b> – </b><b>K</b><b>4 </b><b>P</b><b> – </b><b>K</b><b>4 2. </b><b>Kt</b><b>-</b><b>KB</b><b>3 </b><b>Kt</b><b> – </b><b>QB</b><b>3 3. </b><b>B</b><b> – </b><b>QB</b><b>4 </b><b>B</b><b> – </b><b>QB</b><b>4 4. </b><b>P</b><b> – </b><b>QKt</b><b>4</b> <b>BxQKt</b><b>5</b></p>
